export function Icon({
  name,
  size = 20,
  className = '',
  strokeWidth: _ignored,
  ...rest
}: {
  name: IconName;
  size?: number;
  className?: string;
  strokeWidth?: number;
} & Omit<SVGProps<SVGSVGElement>, 'name'>) {
  const Cmp = REGISTRY[name];
  if (!Cmp) {
    // Fallback to a visible warning glyph in dev so missing names don't silently
    // render as nothing.
    if (process.env.NODE_ENV !== 'production') {
      // eslint-disable-next-line no-console
      console.warn(`[Icon] Unknown icon name: ${String(name)}`);
    }
    return null;
  }
  return <Cmp width={size} height={size} aria-hidden="true" focusable="false" className={className} {...rest} />;
}
